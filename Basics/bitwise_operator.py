x=10
y=4

bitwise_and = x & y
bitwise_or = x | y
bitwise_xor = x ^ y
bitwise_not_x = ~x
left_shift = x << 2
right_shift = y >> 2

print("The Bitwise AND:", bitwise_and)
print("The Bitwise OR:", bitwise_or)
print("The Bitwise XOR:", bitwise_xor)
print("The Bitwise NOT on x:", bitwise_not_x)
print("The Left shift x by 2:", left_shift)
print("The Right shift y by 2:", right_shift)

